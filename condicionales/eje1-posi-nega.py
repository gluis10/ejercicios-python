#Pide al usuario un número e imprime si es positivo, negativo o cero.

print("BIENVENIDO")
numero = int(input("Ingrese un número random: "))

if (numero > 0):
    print("El número es positivo.")
elif (numero < 0):
    print("El número es negativo.")
else: 
    print("El número es cero.")

