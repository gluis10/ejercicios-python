#Rango de números → Pide un número e imprime si está en el rango de 10 a 50 (incluyendo ambos), usando una condición con and.

print("BIENVENIDO")
numero = float(input("- Ingrese un número: "))

if (numero >= 10 and numero <= 50):
    print("Esta en el rango de 10 a 50")
else:
    print("Está fuera del rango")

