-- WHERE EN SQL --

#El where se utiliza para filtrar los registros que cumplen con una condición específica.

#Mostrar toda la información de la tabla users donde la edad sea igual a 15.
SELECT * FROM users WHERE edad = 15;

#Mostrar toda la información de la tabla users donde el año sea mayor a 2020.
SELECT * FROM users WHERE año > 2020;

